    private void limpiaTabla(DefaultTableModel mod) {
        int a = mod.getRowCount();

        while (a != 0) { // Ciclo para Borrar la Tabla 1
            if (a != 0) {
                mod.removeRow(0);
            }
            a = mod.getRowCount();
        }
    }

